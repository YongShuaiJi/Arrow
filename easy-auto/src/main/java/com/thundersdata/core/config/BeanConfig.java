package core.config;

public class BeanConfig {
    private String hub;

    public BeanConfig() {
        super();
        this.hub = hub;
    }

    public BeanConfig(String hub) {
        super();
        this.hub = hub;
    }

    public String getHub() {
        return hub;
    }

    public void setHub(String hub) {
        this.hub = hub;
    }

}
